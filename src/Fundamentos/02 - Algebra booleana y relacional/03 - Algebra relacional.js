/**
 * Los operadores del algebra relacional son
 * 
 ** <   menor que
 ** <=  menor o igual que
 ** >   mayor que
 ** >=  mayor o igual que
 ** ==  igual debil (Compara el valor sin tomar en cuenta el tipo de dato. Es menos preciso)
 ** === igual fuerte (Compara el valor y el tipo de dato. Es mas preciso)
 ** !=  diferente
 ** !   negador (Invierte un valor booleano)
 */

/**
 * Los operandos del algebra relacional son tipicamente NUMEROS,
 * pero se puede utilizar cualquier tipo de dato.
 * 
 * Por ahora solo utilizaremos numeros, operaciones relaciones con
 * otros tipos de dato son un tema mas avanzado.
 * 
 ** El resultado de una operacion relacional SIEMPRE va a ser un valor BOOLEANO (true/false)
 */

/**
 * Ejemplos de operaciones relacionales
 * 
 ** 5 < 10 = VERDADERO
 ** 10 <= 5 = FALSO
 ** 8 > 4 = VERDADERO
 ** 10 >= 10 = VERDADERO
 ** 1 == 1 = VERDADERO
 ** '1' == 1 = VERDADERO
 ** 1 === 1 = VERDADERO
 ** '1' === 1 = FALSO
 ** !FALSO = VERDADERO
 ** !VERDADERO = FALSO
 ** 10 != 10 = VERDADERO
 */
